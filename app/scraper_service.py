    def _upsert_hotel_individual_reviews(
        self,
        session: Session,
        url_obj: URLQueue,
        hotel: Hotel,
        lang: str,
        data: dict,
    ) -> None:
        """
        GAP-SCHEMA-001-FIX (Build 81): Persiste reseñas textuales individuales
        en hotels_individual_reviews.

        Estrategia: delete-before-insert por (url_id, language).
        Consistente con _upsert_hotel_popular_services(),
        _upsert_hotel_all_services(), _upsert_hotel_faqs() y
        _upsert_hotel_guest_reviews() — todas usan el mismo patrón
        para listas variable-length sin clave natural única.

        Si 'individual_reviews' no está en data o es lista vacía, no opera
        (no elimina filas existentes, preservando datos de scrapes anteriores).
        Esto evita borrar reseñas válidas en re-scrapes parciales donde
        Booking.com no renderizó las review-cards (paginación JS).
        """
        from app.models import HotelIndividualReview

        reviews: List[dict] = data.get("individual_reviews") or []

        if not reviews:
            logger.debug(
                "IndividualReviews upsert: 0 reviews in data — skipping lang=%s", lang
            )
            return

        now = _now()

        # Delete previous rows for this url+language before re-inserting
        deleted = (
            session.query(HotelIndividualReview)
            .filter_by(url_id=url_obj.id, language=lang)
            .delete(synchronize_session=False)
        )
        if deleted:
            logger.debug(
                "IndividualReviews upsert: deleted %d stale rows lang=%s", deleted, lang
            )

        inserted = 0
        for rev in reviews:
            # Clamp score to 0-10 constraint (chk_hir_score)
            score = rev.get("score")
            if score is not None:
                try:
                    score = float(score)
                    if not (0.0 <= score <= 10.0):
                        logger.debug(
                            "IndividualReviews: score %.2f out of [0,10] — "
                            "clamped to None lang=%s", score, lang,
                        )
                        score = None
                except (TypeError, ValueError):
                    score = None

            session.add(HotelIndividualReview(
                hotel_id=hotel.id,
                url_id=url_obj.id,
                language=lang,
                reviewer_name=(rev.get("reviewer_name") or None),
                score=score,
                title=(rev.get("title") or None),
                positive_comment=(rev.get("positive_comment") or None),
                negative_comment=(rev.get("negative_comment") or None),
                reviewer_country=(rev.get("reviewer_country") or None),
                booking_id=(rev.get("booking_id") or None),
                created_at=now,
            ))
            inserted += 1

        logger.debug(
            "IndividualReviews upsert: inserted %d rows lang=%s", inserted, lang
        )

    def _upsert_lang_status(
        self,
        url_obj: URLQueue,
        lang: str,
        status: str,
        error: Optional[str],
    ) -> None:
        """Update URLLanguageStatus for this URL+language combination."""
        with get_db() as session:
            existing = (
                session.query(URLLanguageStatus)
                .filter_by(url_id=url_obj.id, language=lang)
                .first()
            )
            now = _now()
            if existing:
                existing.status     = status
                existing.last_error = (error[:2000] if error else None)
                existing.attempts   = (existing.attempts or 0) + 1
                existing.updated_at = now
            else:
                session.add(
                    URLLanguageStatus(
                        url_id=url_obj.id,
                        language=lang,
                        status=status,
                        last_error=(error[:2000] if error else None),
                        attempts=1,
                        created_at=now,
                        updated_at=now,
                    )
                )
            # BUG-DOUBLE-COMMIT-002-FIX (Build 86): session.commit() removed here.
            # get_db() context manager already commits on clean exit.
            # Explicit commit was redundant and caused two round-trips per call.

