    def _upsert_hotel(
        self,
        session: Session,
        url_obj: URLQueue,
        lang: str,
        data: dict,
        duration_ms: int,
    ) -> Hotel:
        """Insert or update the main Hotel row for this URL+language."""
        existing = (
            session.query(Hotel)
            .filter_by(url_id=url_obj.id, language=lang)
            .first()
        )
        now = _now()

        fields = {
            "url_id":            url_obj.id,
            "url":               build_language_url(str(url_obj.base_url or url_obj.url), lang),
            "language":          lang,
            "hotel_name":        data.get("hotel_name"),
            "hotel_id_booking":  data.get("hotel_id_booking"),
            "address_city":      data.get("address_city"),
            "latitude":          data.get("latitude"),
            "longitude":         data.get("longitude"),
            "star_rating":       data.get("star_rating"),
            "review_score":      data.get("review_score"),
            "review_count":      data.get("review_count"),
            "main_image_url":    data.get("main_image_url"),
            "short_description": data.get("short_description"),
            "rating_value":      data.get("rating_value"),
            "best_rating":       data.get("best_rating"),
            "street_address":    data.get("street_address"),
            "address_locality":  data.get("address_locality"),
            "address_country":   data.get("address_country"),
            "postal_code":       data.get("postal_code"),
            "room_types":        data.get("room_types"),
            # STRUCT-019/020 (v76): precio disponibilidad y cantidad tipos habitación
            "price_range":       data.get("price_range"),
            "rooms_quantity":    data.get("rooms_quantity"),
            # GAP-EXTRACT-001-FIX (v76 patch) / BUG-SCHEMA-002-FIX (v77):
            # accommodation_type existia en ORM pero faltaba en schema y en upsert.
            "accommodation_type": data.get("accommodation_type"),
            "raw_data":          data.get("raw_data"),
            "scrape_duration_s": duration_ms / 1_000,
            "scrape_engine":     "selenium",   # Build 63: always selenium
            "updated_at":        now,
        }

        if existing:
            for k, v in fields.items():
                setattr(existing, k, v)
            return existing

